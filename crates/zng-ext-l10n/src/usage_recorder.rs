use std::{collections::HashSet, fs, io, path::PathBuf, sync::LazyLock};

use zng_task::parking_lot::Mutex;
use zng_txt::Txt;

use crate::LangFilePath;

/// (pkg_name, file, id, attribute)
type Key = [Txt; 4];

#[derive(Default)]
struct UsageRecorder {
    used: Mutex<HashSet<Key>>,
}
impl UsageRecorder {
    pub(crate) fn insert(&self, key: Key) {
        let mut used = self.used.lock();
        if !used.contains(&key) {
            used.insert(key);
        }
    }
}

static USED: LazyLock<UsageRecorder> = LazyLock::new(Default::default);

pub(crate) fn insert(file: &LangFilePath, id: &Txt, attribute: &Txt) {
    if !file.is_current_app() {
        USED.insert([file.pkg_name.clone(), file.file.clone(), id.clone(), attribute.clone()]);
    }
}

pub(crate) fn on_init() {
    // assert profile path is valid
    let _ = env_file();

    zng_app::APP.on_deinit(|_| save());
}

fn collect() -> String {
    use std::fmt::Write as _;

    let mut s = String::new();

    writeln!(&mut s, "# Auto generated by zng-ext-10n usage recorder, don't edit").unwrap();
    writeln!(&mut s, "# Add manual selections to file `./{{name}}.subset`, without `.rec.`").unwrap();
    writeln!(&mut s).unwrap();

    let mut used: Vec<_> = USED.used.lock().drain().collect();
    used.sort();
    for [pkg_name, file, id, attribute] in used {
        write!(&mut s, "{pkg_name}//").unwrap();
        if !file.is_empty() && file != "_" {
            write!(&mut s, "{file}/").unwrap();
        }
        write!(&mut s, "{id}").unwrap();
        if !attribute.is_empty() {
            write!(&mut s, ".{attribute}").unwrap();
        }
        writeln!(&mut s).unwrap();
    }
    s
}

fn env_file() -> Option<String> {
    let p = std::env::var("ZNG_L10N_PROFILE_FILE").ok()?;
    if !p.ends_with(".rec.subset") {
        panic!("ZNG_L10N_PROFILE_FILE must be set to a file with extension `.rec.subset`")
    }
    Some(p)
}
fn save() {
    fn write(profile: &str) -> io::Result<()> {
        let path = env_file().unwrap_or_else(|| "res/optimization-profiles/zng-ext-10n.rec.subset".to_owned());
        let path = PathBuf::from(path);
        if let Some(dir) = path.parent() {
            fs::create_dir_all(dir)?;
        }
        fs::write(path, profile.as_bytes())
    }
    let profile = collect();

    if let Err(e) = write(&profile) {
        panic!("failed to save l10n usage profile\n{e}\n\n```zng-ext-10n.rec.subset\n{profile}\n```")
    }
}
