use std::{collections::HashSet, fs, io, path::PathBuf};

use zng_wgt::prelude::task::parking_lot::Mutex;

#[derive(Default)]
pub(crate) struct UsageRecorder {
    used: Mutex<HashSet<String>>,
}
impl UsageRecorder {
    pub(crate) fn insert(&self, key: &str) {
        let mut used = self.used.lock();
        if !used.contains(key) {
            used.insert(key.to_owned());
        }
    }
}

pub(crate) fn on_init() {
    // assert profile path is valid
    let _ = env_file();

    zng_app::APP.on_deinit(|_| save());
}

fn collect() -> String {
    use std::fmt::Write as _;

    let mut s = String::new();

    writeln!(&mut s, "# Auto generated by zng-wgt-material-icons usage recorder, don't edit").unwrap();
    writeln!(&mut s, "# Add manual selections to file `./{{name}}.subset`, without `.rec.`").unwrap();
    writeln!(&mut s).unwrap();

    let sets = [
        #[cfg(feature = "filled")]
        ("filled", &super::filled::USAGE),
        #[cfg(feature = "outlined")]
        ("outlined", &super::outlined::USAGE),
        #[cfg(feature = "rounded")]
        ("rounded", &super::rounded::USAGE),
        #[cfg(feature = "sharp")]
        ("sharp", &super::sharp::USAGE),
    ];
    for (set, used) in sets {
        let mut used: Vec<_> = used.used.lock().drain().collect();
        used.sort();
        for used in used {
            writeln!(&mut s, "{set}/{used}").unwrap();
        }
    }
    s
}

fn env_file() -> Option<String> {
    let p = std::env::var("ZNG_MATERIAL_ICONS_PROFILE_FILE").ok()?;
    if !p.ends_with(".rec.subset") {
        panic!("ZNG_MATERIAL_ICONS_PROFILE_FILE must be set to a file with extension `.rec.subset`")
    }
    Some(p)
}
fn save() {
    fn write(profile: &str) -> io::Result<()> {
        let path = env_file().unwrap_or_else(|| "res/optimization-profiles/zng-wgt-material-icons.rec.subset".to_owned());
        let path = PathBuf::from(path);
        if let Some(dir) = path.parent() {
            fs::create_dir_all(dir)?;
        }
        fs::write(path, profile.as_bytes())
    }
    let profile = collect();

    if let Err(e) = write(&profile) {
        panic!("failed to save icon usage profile\n{e}\n\n```zng-wgt-material-icons.rec.subset\n{profile}\n```")
    }
}
